package com.bdstar.www.xmlbuild.beans;

public class PressBean {
    public static final String alias="PressBean";
    public String link;
    public String x;
    public String y;

    public String getLink() {
        return link;
    }

    public void setLink(String link) {
        this.link = link;
    }

    public String getX() {
        return x;
    }

    public void setX(String x) {
        this.x = x;
    }

    public String getY() {
        return y;
    }

    public void setY(String y) {
        this.y = y;
    }
}
